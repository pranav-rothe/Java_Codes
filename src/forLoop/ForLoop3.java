package forLoop;

public class ForLoop3 {

	public static void main(String[] args) {
		
		//WAP to print a 50 to 70 number.".
		
		//syntax:- for(initialization; condition; increment/decrement){
						//statement
		//		   }
		
		for(int i=50;i<=70;i++) {
			System.out.print(i+"  ");
		}
		System.out.println();
		System.out.println("Program Completed.");
	}

}
