package forLoop;

public class ForLoop2 {

	public static void main(String[] args) {
		
		//WAP to print a 1 to 5 number.".
		
		//syntax:- for(initialization; condition; increment/decrement){
						//statement
		//		   }
		
		for(int i=1;i<6;i++) {
			System.out.print(i+" ");
		}
		System.out.println();
		System.out.println("Program Completed.");
	}

}
