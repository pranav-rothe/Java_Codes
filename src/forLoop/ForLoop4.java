package forLoop;

public class ForLoop4 {

	public static void main(String[] args) {
		
		//WAP to print a 10 to 1 number.".
		
		//syntax:- for(initialization; condition; increment/decrement){
						//statement
		//		   }
		
		for(int i=10;i>=1;i--) {
			System.out.print(i+"  ");
		}
		System.out.println();
		System.out.println("Program Completed.");
	}

}
