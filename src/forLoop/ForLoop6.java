package forLoop;

public class ForLoop6 {

	public static void main(String[] args) {
		
		//WAP to addition of 1 to 5 number.".
		
		//syntax:- for(initialization; condition; increment/decrement){
						//statement
		//		   }
		int addition=0;
		for(int i=1;i<=5;i++) {
			addition=addition+i;
		}
		System.out.println("Addtion of 1 to 5 number: "+addition);
		System.out.println("Program Completed.");
		
	}

}
